
public class Calc {

	public static int sottrazione(int x, int y) {

		int ris = x - y;

		return ris;

	}

	public static int addizione(int x, int y) {
		int ris = x + y;

		return ris;

	}

	public static int moltiplicazione(int x, int y) {
		int ris = x * y;

		return ris;
	}

	public static int divisione(int x, int y) {
		int ris = x / y;

		return ris;
	}
	public static int percentuale (int x, int y) {
		int ris= (y*100)/x;
		return ris; 
	}
}

